     // Metode yang dipanggil saat tombol register diklik
    private void registerBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBTNActionPerformed
        // Mengambil input dari pengguna
        String username = usernameTF.getText();
        String password = new String(passwordTF.getPassword());
        String confirmPW = new String(confirmPwTF.getPassword());

        // Validasi jika password atau konfirmasi password kosong
        if (username.isEmpty() && password.isEmpty() && confirmPW.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan Username dan Password untuk registrasi."); //akan muncul pesan peringatan ini
        return; // Hentikan proses jika validasi gagal
        }
        
        // Validasi jika password atau konfirmasi password kosong
        if (password.isEmpty() || confirmPW.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Password dan konfirmasi password tidak boleh kosong."); //akan muncul pesan peringatan ini
        return; // Hentikan proses jika validasi gagal
        }
        
        // Validasi input jika username yang dibuat hanya 6 karakter
        if (username.length() <= 6) {
            JOptionPane.showMessageDialog(this, "Username harus lebih dari 6 karakter."); //akan muncul pesan peringatan ini
            return; // Hentikan proses jika validasi gagal
        }

        //apabila password tidak sesuai dengan konfirmasi password
        if (!password.equals(confirmPW)) {
            JOptionPane.showMessageDialog(this, "Password dan konfirmasi password tidak sesuai."); //akan muncul pesan peringatan ini
            return; // Hentikan proses jika validasi gagal
        }

        // Jika semua validasi berhasil, lanjutkan dengan registrasi
        if (MyJDBC.register(username, password)) {
            this.dispose(); // Menutup jendela register saat registrasi berhasil
            login login = new login();
            JOptionPane.showMessageDialog(login, "Registrasi akun berhasil!");
            login.setVisible(true); // Menampilkan jendela login
        } else { //kondisi yang akan dijalankan apabila username sudah ada dalam database
            JOptionPane.showMessageDialog(this, "Registrasi akun gagal! \nUsername telah terdaftar silahkan buat dengan username baru");
        }
    }//GEN-LAST:event_registerBTNActionPerformed

    private void addloginClickListener(){
        // Menambahkan listener mouse ke komponen `loginLB`.
        loginLB.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e){
                register.this.dispose(); // Menutup form registrasi saat komponen diklik.
                
                new login().setVisible(true);  // Membuka form login baru.
            }
        });
    }
    
     private boolean validasiUserInput(String username, String password, String confirmPass){
        // Memeriksa apakah username, password, atau konfirmasi password kosong.
        if(username.length() == 0 || password.length() == 0 || confirmPass.length() == 0) return false;
        // Memeriksa apakah panjang username kurang dari 6 karakter.
        if(username.length() < 6) return false;
        // Memeriksa apakah password dan konfirmasi password tidak cocok.
        if(!password.equals(confirmPass)) return false;
        // Jika semua validasi lulus, kembalikan true.
        return true;
    }

    

    
