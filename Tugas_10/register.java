    private void lihatPassCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatPassCBActionPerformed
        // Jika checkbox `lihatPassCB` dipilih, tampilkan teks password dan konfirmasi password.
        if(lihatPassCB.isSelected()){
            confirmPwTF.setEchoChar((char)0); // Menampilkan teks secara jelas.
            passwordTF.setEchoChar((char)0); // Menampilkan teks secara jelas.
        }else{ // Mengembalikan teks ke mode password (disamarkan).
           confirmPwTF.setEchoChar('*');
           passwordTF.setEchoChar('*'); 
        }
    }//GEN-LAST:event_lihatPassCBActionPerformed

