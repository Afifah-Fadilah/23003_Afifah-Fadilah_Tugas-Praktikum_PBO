    private void addRegisterClickListener(){ 
        // Menambahkan listener yang menangani klik mouse pada registerLB.
        registerLB.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e){
                login.this.dispose(); // Menutup jendela login saat ini.
                
                new register().setVisible(true);  // Membuka jendela registrasi.
            }
        });
    }
    
    // Method yang menangani aksi tombol login ketika diklik.
    private void loginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBTNActionPerformed
        // Mengambil teks dari field username dan password.
        String username = usernameTF.getText(); 
        String password = new String(passwordTF.getPassword());
        
        // Memvalidasi login dengan memanggil metode dari kelas MyJDBC.
        if(MyJDBC.validasiLogin(username, password)){ // Menampilkan pesan sukses jika validasi berhasil.
            JOptionPane.showMessageDialog(login.this, "Login Berhasil!");
            new kalkulator().setVisible(true); //dan form kalkulator ditampilkan setelah pesan login berhasil
            this.dispose(); // Menutup form login saat ini.
        }else{ // Menampilkan pesan kesalahan jika validasi gagal.
            JOptionPane.showMessageDialog(login.this, "Login gagal!\nMasukkan Username dan Password dengan benar!");
        }
    }//GEN-LAST:event_loginBTNActionPerformed

