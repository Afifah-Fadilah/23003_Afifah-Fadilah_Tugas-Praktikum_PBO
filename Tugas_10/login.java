    // Method untuk menangani event checkbox 'lihat password' diaktifkan atau dinonaktifkan.
    private void lihatPassCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatPassCBActionPerformed
        if(lihatPassCB.isSelected()){ // Jika checkbox dipilih, tampilkan password dengan karakter biasa (tidak disembunyikan).
            passwordTF.setEchoChar((char)0);
        }else{
           passwordTF.setEchoChar('*');   // Jika checkbox tidak dipilih, sembunyikan password dengan karakter '*'.
        }
    }//GEN-LAST:event_lihatPassCBActionPerformed

