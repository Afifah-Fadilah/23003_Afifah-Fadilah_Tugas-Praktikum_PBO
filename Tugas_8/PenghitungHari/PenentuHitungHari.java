    //membuat pengaturan untuk button hitung
    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        if(tahun.getText().isEmpty()){ //pengkondiasian apabila tahun kosong,maka
            JOptionPane.showMessageDialog(null, "Error, isian tahun kosong",
            "Error", JOptionPane.WARNING_MESSAGE); //akan menampilkan pesan eror
        }else{ //dan apabila tahun tidak kosong maka
            int year = Integer.parseInt(tahun.getText()); // Mengonversi teks dari input 'tahun' menjadi tipe integer
            String month = bulan.getSelectedItem().toString(); // Mengambil item yang dipilih dari komponen combobox 'bulan' sebagai String
            int jumlah=HitungHari.hitung(year, month); // Menghitung jumlah hari pada bulan dan tahun yang dipilih menggunakan metode HitungHari.hitung
            
            jumlahHari.setText("Jumlah hari pada Bulan "+month+ " Tahun " + year+ " adalah " + jumlah + " Hari"); //menampilkan jumlah hari pada bulan dan tahun yang cari
        }
    }//GEN-LAST:event_hitungActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed

    // Mengambil teks dari jumlahHari
    String data = jumlahHari.getText().trim(); // .trim() untuk menghapus spasi atau baris baru yang tidak perlu

    if(tahun.getText().isEmpty()){ //membuat kondisi apabila tahun kosong maka,
        JOptionPane.showMessageDialog(null, "Isian tahun kosong, data tidak dapat disimpan",
        "Error", JOptionPane.WARNING_MESSAGE); //akan menampilkan pesan error dan data tidak di simpan
    }else{ //apabila tahun tidak kosong maka
        // Membuat objek JFileChooser untuk membuka dialog penyimpanan file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan File"); // Memberikan judul pada dialog penyimpanan

        int userSelection = fileChooser.showSaveDialog(this);// Menampilkan dialog penyimpanan dan menunggu pilihan pengguna
        if (userSelection == JFileChooser.APPROVE_OPTION) { // kondisi untuk Memeriksa apakah pengguna mengklik tombol "Save"
            File fileToSave = fileChooser.getSelectedFile(); //maka Mendapatkan file yang dipilih/dimasukkan oleh pengguna
            
            // Menulis data ke file yang dipilih
            try (BufferedWriter out = new BufferedWriter(new FileWriter(fileToSave))) {
                out.write(data); // Menulis teks 'data' ke dalam file
                JOptionPane.showMessageDialog(null, "Berhasil disimpan dalam file: " + fileToSave.getAbsolutePath()); // Menampilkan pesan konfirmasi bahwa file berhasil disimpan
            } catch (IOException e) {
                System.err.println("Error: " + e.getMessage()); // Mencetak pesan error ke console jika terjadi masalah saat menyimpan
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan file: " + e.getMessage()); // Menampilkan pesan error jika terjadi kesalahan saat menyimpan file
            }
        }
    }
    }//GEN-LAST:event_simpanActionPerformed

