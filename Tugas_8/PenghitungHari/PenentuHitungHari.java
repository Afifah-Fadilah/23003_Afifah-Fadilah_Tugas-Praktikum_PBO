    private void tahunKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tahunKeyTyped
        char c = evt.getKeyChar();// Mendapatkan karakter yang diketik oleh pengguna
        // Mengecek apabila karakter bukan digit, bukan tombol BACKSPACE, dan bukan tombol DELETE
        if(!((Character.isDigit(c)||(c==KeyEvent.VK_BACK_SPACE)||(c==KeyEvent.VK_DELETE)))){
            getToolkit().beep(); // Membunyikan beep untuk memberi tahu pengguna bahwa input salah
            JOptionPane.showMessageDialog(null, "Masukkan hanya bilangan angka!"); //menampilkan pesan peringatan
            
            evt.consume(); // Membatalkan event agar karakter yang tidak valid tidak dimasukkan ke dalam input
        }
    }//GEN-LAST:event_tahunKeyTyped

